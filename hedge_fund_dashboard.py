def download_data(tickers, start, end):
    raw = yf.download(tickers, start=start, end=end, auto_adjust=True, progress=False)
    if len(tickers) == 1:
        prices = raw[["Close"]].rename(columns={"Close": tickers[0]})
    else:
        prices = raw["Close"]
    prices.dropna(how="all", inplace=True)
    return prices

def compute_metrics(prices):
    returns = prices.pct_change().dropna()
    ann_return = (1 + returns.mean()) ** 252 - 1
    ann_vol = returns.std() * np.sqrt(252)
    rf = 0.05
    sharpe = (ann_return - rf) / ann_vol
    downside = returns.copy()
    downside[downside > 0] = 0
    ann_downside = downside.std() * np.sqrt(252)
    sortino = (ann_return - rf) / ann_downside
    cum = (1 + returns).cumprod()
    rolling_max = cum.cummax()
    drawdown = (cum - rolling_max) / rolling_max
    max_dd = drawdown.min()
    metrics = pd.DataFrame({
        "ann_return": ann_return, "volatility": ann_vol,
        "sharpe": sharpe, "sortino": sortino, "max_dd": max_dd,
    })
    return metrics, returns, cum, drawdown

def optimize_portfolio(returns, risk_tolerance=0.5):
    try:
        import cvxpy as cp
        mu = returns.mean().values * 252
        Sigma = returns.cov().values * 252
        n = len(mu)
        w = cp.Variable(n)
        lam = 1 - risk_tolerance
        objective = cp.Maximize(mu @ w - lam * cp.quad_form(w, Sigma))
        constraints = [cp.sum(w) == 1, w >= 0.02, w <= 0.60]
        prob = cp.Problem(objective, constraints)
        prob.solve(solver=cp.CLARABEL, warm_start=True)
        if prob.status in ["optimal", "optimal_inaccurate"]:
            weights = np.array(w.value)
            weights = np.clip(weights, 0, 1)
            weights /= weights.sum()
            return dict(zip(returns.columns, weights))
    except Exception:
        pass
    ann_return = returns.mean() * 252
    ann_vol = returns.std() * np.sqrt(252)
    sharpe = (ann_return - 0.05) / ann_vol
    sharpe_pos = sharpe.clip(lower=0.01)
    weights = sharpe_pos / sharpe_pos.sum()
    return dict(zip(returns.columns, weights.values))

def generate_insights(metrics, returns, t):
    insights = {"increase": [], "decrease": [], "diversify": []}
    n = len(metrics)
    corr = returns.corr()
    for ticker in metrics.index:
        s = metrics.loc[ticker, "sharpe"]
        v = metrics.loc[ticker, "volatility"]
        dd = metrics.loc[ticker, "max_dd"]
        avg_corr = corr[ticker].drop(ticker).mean() if n > 1 else 0
        if s > 1.0 and v < 0.30:
            insights["increase"].append(f"<b>{ticker}</b> — {t['sharpe_lbl']} {s:.2f} | Vol {v:.1%} | {t['insight_increase']}")
        elif s < 0.3 or dd < -0.35:
            insights["decrease"].append(f"<b>{ticker}</b> — {t['sharpe_lbl']} {s:.2f} | {t['vol_lbl']} {dd:.1%} | {t['insight_decrease']}")
        if n > 1 and avg_corr > 0.75:
            insights["diversify"].append(f"<b>{ticker}</b> — {t['avg_corr_lbl']} {avg_corr:.2f} | {t['insight_diversify']}")
    return insights

