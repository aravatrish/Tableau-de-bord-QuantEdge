    pdf.set_font("Helvetica", "B", 9)
    pdf.set_fill_color(20, 30, 60)
    pdf.set_text_color(200, 220, 255)
    for i, h in enumerate(headers):
        pdf.cell(col_w[i], 8, h, border=1, fill=True, align="C")
    pdf.ln()
    pdf.set_font("Helvetica", "", 9)
    for idx, row in metrics.iterrows():
        pdf.set_text_color(30, 30, 50)
        vals = [str(idx), f"{row['ann_return']:.1%}", f"{row['volatility']:.1%}", f"{row['sharpe']:.2f}", f"{row['sortino']:.2f}", f"{row['max_dd']:.1%}"]
        for i, v in enumerate(vals):
            pdf.cell(col_w[i], 7, v, border=1, align="C")
        pdf.ln()
    pdf.ln(8)
    pdf.set_font("Helvetica", "B", 13)
    pdf.set_text_color(30, 30, 50)
